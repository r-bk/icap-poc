#[inline]
pub fn is_token(b: u8) -> bool {
    debug_assert!(TOKEN_MAP.len() == 256);
    unsafe { *TOKEN_MAP.get_unchecked(b as usize) }
}

#[inline]
pub fn is_text(b: u8) -> bool {
    debug_assert!(TEXT_MAP.len() == 256);
    unsafe { *TEXT_MAP.get_unchecked(b as usize) }
}

#[inline]
pub fn is_spht(b: u8) -> bool {
    debug_assert!(SPHT_MAP.len() == 256);
    unsafe { *SPHT_MAP.get_unchecked(b as usize) }
}

#[inline]
pub fn has_quoted_prefix(bytes: &[u8], idx: usize) -> bool {
    bytes[idx - 1] == b'\\'
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_token_map() {
        assert_eq!(TOKEN_MAP.len(), 256);
        for (idx, v) in TOKEN_MAP.iter().enumerate() {
            let idx = idx as u8;
            if !v {
                assert!(
                    idx <= 31
                        || idx >= 127
                        || idx == b'('
                        || idx == b')'
                        || idx == b'<'
                        || idx == b'>'
                        || idx == b'@'
                        || idx == b','
                        || idx == b':'
                        || idx == b';'
                        || idx == b'\\'
                        || idx == b'"'
                        || idx == b'/'
                        || idx == b'['
                        || idx == b']'
                        || idx == b'?'
                        || idx == b'='
                        || idx == b'{'
                        || idx == b'}'
                        || idx == b' '
                        || idx == b'\t'
                );
            }
        }
    }

    #[test]
    fn test_is_text() {
        assert_eq!(TEXT_MAP.len(), 256);
        for (idx, v) in TEXT_MAP.iter().enumerate() {
            let idx = idx as u8;
            if !v {
                assert!((idx <= 31 && idx != b' ' && idx != b'\t') || idx == 127);
            } else {
                assert!((idx > 31 && idx != 127) || idx == b' ' || idx == b'\t');
            }
        }
    }

    #[test]
    fn test_is_spht() {
        assert_eq!(SPHT_MAP.len(), 256);
        for (idx, v) in SPHT_MAP.iter().enumerate() {
            let idx = idx as u8;
            if *v {
                assert!(idx == b' ' || idx == b'\t');
            } else {
                assert!(idx != b' ' && idx != b'\t');
            }
        }
    }
}
